#include "libft.h"

int	ft_strchri(const char *s, int c)
{
	int	z;

	z = 0;
	if (s && c)
	{
		while (s[z])
		{
			if (s[z] == c)
				return (z);
			z++;
		}
		return (-1);
	}
	return (-1);
}
